  <p align = "justify">
Inicio de labores en 1996, con el Instituto de Ciencias Socio-Familiares del Colegio Mayor de San Buenaventura. 
El primer programa que ofreció el insituto - seccional Medellín fue el de Licenciatura en Ciencias Socio-Familiares, que luego 
se denominó: Sociología. El fundador y primer decano fue Fray Arturo Calle Restrepo. Las primeras tres facultades fueron: Ciencias Socio-Familiares 
(Sociología), Psicología y Educación. Nuestro primer posgrado se inició en 1990, siendo rector Pablo Castillo Nova. En un principio se denominó Especialización 
en Administración de la Educación Preescolar, pero en 1994, se denominó Especialización en Administración de la Educación.
</p>
<p align = "justify">
Nuestro segundo posgrado y primera Maestría fue la Maestría en Neuropsicología, creada en 1995, bajo la rectoría de Germán Alfonso Bautista Romero. 
En ese mismo año se fundó la casa de posgrados; ubicada en el sector estadio. Al respecto escribía el Director académico General de la época: Darío 
Ernesto Jaén Navarro, refiriéndose a ella como casa del saber “la educación posgradual formal tiene la misión no solo de cualificar en sentido específico 
los conocimientos de un disciplina o profesión sino de proponer, mediante la investigación, alternativas de aplicación: este es el espacio donde se 
constituye la sabiduría”. En la casa de posgrados se desarrollaron actividades de formación avanzada hasta el año 2005.
</p>
<p align = "justify">
Posteriormente los programas académicos de posgrados se ofrecen en las instalaciones de la sede San Benito y se sigue ampliando la oferta de posgrados 
en las diferentes facultades. Ante este aumento y la alta demanda del medio en formación avanzada, en el año 2014, realiza una adecuación importante de sus 
aulas del bloque F en su sede San Benito, para generar las condiciones apropiadas, con el fin de ofertar las 
especializaciones, maestrías y doctorados, en un mejor entorno de estudio, permitiendo que el espacio se adecúe a las necesidades y procesos formativos 
de los estudiantes.
</p>
<p align = "justify">
En el año 2017, desde la Rectoría de José Alirio Urbina Rodríguez, las directivas de la Universidad analizan la necesidad de mejorar de forma 
sustancial las condiciones de ofrecimiento de los posgrados, no sólo en términos del espacio de las aulas, sino además de óptimas condiciones en el entorno 
interno y externo del cual se beneficien los estudiantes que, por su perfil, requieran acceso a servicios adicionales, con mayor agilidad, seguridad y calidad. 
Se analizó también que el espacio contara con suficientes parqueaderos, oferta de esparcimiento, entretenimiento y alimentación, entre otros. Luego de estudiar 
varias opciones de ubicación, se optó por ampliar el desarrollo de la oferta de los programas de posgrados a un sitio que garantizara continuar con programas de 
calidad, que respondiera a las demandas académicas y requerimientos tecnológicos y de infraestructura para un excelente desarrollo de los mismos. Es así como a 
finales del mes de febrero del 2018, el Instituto ACV seccional Medellín amplia el ofrecimiento de su formación posgradual a su nuevo Centro de 
Formación Avanzada: Juan Duns Escoto.
</p>

